    public void CheckChooiseDate() {
        Boolean flag = true;
        if (DateStart.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Vui chọn ngày bắt đầu!");
            flag = false;
        }
        if (DateEnd.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Vui chọn ngày kết thúc");
            flag = false;
        }

    }

